import * as fs from 'fs';
import * as path from 'path';
import { fetchTrending, pickTopNew } from './trending';
import { loadHistory, saveHistory, getExistingRepos, addRecord } from './history';
import { analyzeRepo } from './analyzer';

const REPORTS_DIR = path.resolve(__dirname, '..', 'reports');

function ensureReportsDir() {
  if (!fs.existsSync(REPORTS_DIR)) fs.mkdirSync(REPORTS_DIR, { recursive: true });
}

function generateReport(repo: ReturnType<typeof pickTopNew>, analysis: string): string {
  if (!repo) return '';
  const date = new Date().toISOString().slice(0, 10);
  const repoSlug = repo.name.replace(/\//g, '-');
  const filename = `${date}-${repoSlug}.md`;

  const content = `# ${repo.name} - GitHub Trending Analysis

> 📅 ${date} | ⭐ ${repo.stars} stars (+${repo.todayStars} today) | 🔧 ${repo.language || 'N/A'}

${analysis}

---
*Generated by github-trending-analyzer*
`;

  ensureReportsDir();
  const filepath = path.join(REPORTS_DIR, filename);
  fs.writeFileSync(filepath, content, 'utf-8');
  return filename;
}

async function main() {
  console.log('🚀 GitHub Trending Daily Analyzer');
  console.log('='.repeat(40));

  // 1. 加载历史
  const history = loadHistory();
  const existing = getExistingRepos(history);
  console.log(`📋 历史记录: ${existing.length} 个项目`);

  // 2. 抓取 Trending
  console.log('🌐 正在抓取 GitHub Trending...');
  let repos;
  try {
    repos = await fetchTrending();
    console.log(`✅ 抓取到 ${repos.length} 个 trending 项目`);
  } catch (err: any) {
    console.error(`❌ 抓取失败: ${err.message}`);
    process.exit(1);
  }

  // 3. 去重选第一个新项目
  const repo = pickTopNew(repos, existing);
  if (!repo) {
    console.log('⚠️ 今日所有 trending 项目均已分析过，跳过');
    return;
  }
  console.log(`📦 选中: ${repo.name} (${repo.language || 'N/A'}, ⭐${repo.stars}, +${repo.todayStars} today)`);

  // 4. Claude 分析
  console.log('🤖 正在调用 Claude 分析...');
  const analysis = analyzeRepo(repo);
  console.log('✅ 分析完成');

  // 5. 生成报告
  const reportFile = generateReport(repo, analysis);
  console.log(`📄 报告已生成: reports/${reportFile}`);

  // 6. 更新历史
  const date = new Date().toISOString().slice(0, 10);
  addRecord(history, {
    date,
    repo: repo.name,
    stars: repo.stars,
    language: repo.language,
    todayStars: repo.todayStars,
    reportFile: `reports/${reportFile}`,
  });
  saveHistory(history);
  console.log('💾 历史记录已更新');
  console.log('='.repeat(40));
  console.log('✅ 完成!');
}

main().catch(err => {
  console.error('Fatal error:', err);
  process.exit(1);
});
